package com.wipro.java.oops.abstraction;

public class Honda extends Car{

	/**
	 * Implementing the abstract methods in child class
	 */
	
	
	@Override
	void carStart() 
	{
		System.out.println("Honda car started");
	}

	@Override
	void carStop() 
	{
		System.out.println("Honda car started");
	}

}
